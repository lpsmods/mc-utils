export type FontDef = Record<string, string[]>;

export enum Fonts {
  Default = "default",
  Alt = "alt",
  Illageralt = "illageralt",
}

export class Font {
  static fonts = new Map<string, FontDef>();

  /**
   * Get a font by name.
   * @param {Fonts|string} fontName
   * @returns {FontDef|undefined}
   */
  static get(fontName: Fonts | string): FontDef {
    const res = Font.fonts.get(fontName.toString().toLowerCase());
    if (!res) throw new Error(`Font ${fontName} not found!`);
    return res;
  }
}

